    private void lihatDataAnggota(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lihatDataAnggota
        // TODO add your handling code here:
        int x = tabelAnggota.getSelectedRow();
        if (x != -1){
            id.setText(tabelAnggota.getValueAt(x,0).toString());
            nama.setText(tabelAnggota.getValueAt(x,1).toString());
            nrp.setText(tabelAnggota.getValueAt(x,2).toString());
            pangkat.setText(tabelAnggota.getValueAt(x,3).toString());
            
        }
    }//GEN-LAST:event_lihatDataAnggota

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Apakah anda yakin ingin menghapus data anggota "+nama.getText()+" ?", "Hapus anggota", dialogButton);
        if(dialogResult == 0) {
          try {
            anggota.hapus(id.getText());
            } finally {
                JOptionPane.showMessageDialog(rootPane, "data anggota berhasil dihapus");
                showTable(anggota.data());
                id.setText("...");
                nama.setText("");
                nrp.setText("");
                pangkat.setText("");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            anggota.update(id.getText(), nama.getText(),nrp.getText(),pangkat.getText());
        } finally {
            JOptionPane.showMessageDialog(rootPane, "data anggota berhasil diedit");
            showTable(anggota.data());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

