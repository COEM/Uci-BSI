    private void tabelSimpananMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelSimpananMouseClicked
        // TODO add your handling code here:
        int x = tabelSimpanan.getSelectedRow();
        if (x != -1){
            no_simpan.setText(tabelSimpanan.getValueAt(x,0).toString());
            id_anggota.setText(tabelSimpanan.getValueAt(x,1).toString());
            wajib.setText(tabelSimpanan.getValueAt(x,3).toString());
            sukarela.setText(tabelSimpanan.getValueAt(x,2).toString());
            tanggal.setText(tabelSimpanan.getValueAt(x,4).toString());
            
        }
    }//GEN-LAST:event_tabelSimpananMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Apakah anda yakin ingin menghapus data simpanan "+no_simpan.getText()+" ?", "Hapus Simpanan", dialogButton);
        if(dialogResult == 0) {
          try {
            simpanan.hapus(no_simpan.getText());
            } finally {
                JOptionPane.showMessageDialog(rootPane, "data anggota berhasil dihapus");
                showTable(simpanan.data());
                no_simpan.setText("...");
                id_anggota.setText("");
                wajib.setText("");
                sukarela.setText("");
                tanggal.setText("");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            simpanan.update(no_simpan.getText(),id_anggota.getText(),Integer.parseInt(sukarela.getText()), Integer.parseInt(wajib.getText()),tanggal.getText());
        } finally {
            JOptionPane.showMessageDialog(rootPane, "data simpanan berhasil diedit");
            showTable(simpanan.data());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

